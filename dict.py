# Create a dictionary
student = {
    "name": "Joshua",
    "age": 18,
    "course": "Computer Science"
}

# Display dictionary
print("Student info:", student)

# Access a value
print("Name:", student["name"])

# Update a value
student["age"] = 19

# Add a new key-value pair
student["grade"] = "A"

# Display updated dictionary
print("Updated student info:", student)
